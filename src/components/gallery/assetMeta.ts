export interface Generation {
  workflow: string
  model: string
  steps: number
  /** Absent for promptless workflows (upscale / hi-res passes) — hide the field. */
  prompt?: string
}

export function mockGeneration(name: string): Generation {
  const h = hash(name)
  const model = at(MODELS, h)
  const fast = /schnell|turbo|lightning/i.test(model)
  // ~1 in 5 assets is a promptless upscale/hi-res pass (Prompt field hidden).
  const promptless = (h >>> 11) % 5 === 0
  return {
    workflow: promptless ? at(PROMPTLESS_WORKFLOWS, h >>> 5) : at(WORKFLOWS, h >>> 5),
    model,
    // schnell/turbo run at very low step counts; everything else 20–50.
    steps: fast ? at([4, 6, 8], h >>> 2) : at([20, 24, 28, 30, 35, 40, 50], h >>> 2),
    prompt: promptless ? undefined : at(PROMPTS, h >>> 9),
  }
}
