// Deterministic-but-varied mock "Generation details" for an asset (prototype
// only). Derived from the asset name's hash so a given asset always shows the
// same realistic-looking workflow / model / steps / prompt across the dock
// popover AND the tab details panel.

function hash(s: string) {
  let h = 0
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0
  return h
}
// NOTE: callers derive sub-indices with the UNSIGNED shift `>>>` (not `>>`) —
// a signed shift on a hash with the high bit set goes negative, and a negative
// index here returns `undefined`. `at` also normalises negatives defensively.
const at = <T,>(arr: readonly T[], n: number) => arr[((n % arr.length) + arr.length) % arr.length]!

// Mocked file size / created date derived from the name hash (no real metadata in
// the prototype). Same formulas the detail panels use, exported so the Quick Look
// preview shows consistent values. (The detail panels still keep local copies.)
export function mockSize(name: string) {
  return `${(2 + (hash(name) % 180) / 10).toFixed(1)} MB`
}
export function mockDate(name: string) {
  const h = hash(name)
  const d = new Date(2025, h % 12, (h % 28) + 1, h % 24, h % 60, (h >> 3) % 60)
  const date = d.toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' })
  let hr = d.getHours()
  const ampm = hr >= 12 ? 'pm' : 'am'
  hr = hr % 12 || 12
  const time = `${hr}:${String(d.getMinutes()).padStart(2, '0')}:${String(d.getSeconds()).padStart(2, '0')}${ampm}`
  return { date, time }
}

/* ── Time-based media mocks (video / audio) ───────────────────────────────────
   Deterministic from the name hash so a clip/track always shows the same
   duration, fps, sample-rate, and waveform across the card, Quick Look, and the
   lightbox. No real media files in the prototype. */
export function mockDuration(name: string): string {
  const secs = 5 + (hash(name) % 596) // 5s .. ~10min
  const m = Math.floor(secs / 60)
  return `${m}:${String(secs % 60).padStart(2, '0')}`
}
const FPS = ['24fps', '30fps', '60fps'] as const
export const mockFps = (name: string) => at(FPS, hash(name) >>> 6)
const SAMPLE_RATES = ['44.1kHz', '48kHz'] as const
export const mockSampleRate = (name: string) => at(SAMPLE_RATES, hash(name) >>> 4)

// 3D model polygon count, e.g. "24k tris".
export const mockPolyCount = (name: string) => `${5 + (hash(name) % 142)}k tris`

/** Deterministic bar heights (0..1) for a fake audio waveform. An arch envelope
 *  (taller in the middle) over per-bar pseudo-random noise so it reads as audio. */
export function mockWaveform(name: string, bars = 48): number[] {
  let seed = hash(name) || 1
  const out: number[] = []
  for (let i = 0; i < bars; i++) {
    seed = (seed * 1103515245 + 12345) >>> 0
    const noise = (seed % 1000) / 1000 // 0..1
    const arch = 0.4 + 0.6 * Math.sin((Math.PI * i) / (bars - 1)) // 0.4..1
    out.push(Math.max(0.1, noise * arch))
  }
  return out
}

